/**
 * Traduz uma tag do inglês para português
 * Retorna a tag original se não houver tradução disponível
 */
export function translateTag(tag: string): string {
  const lower = tag.toLowerCase().trim();
  return TAG_TRANSLATIONS[lower] || tag;
}

/**
 * Traduz múltiplas tags
 */
export function translateTags(tags: string[]): string[] {
  return tags.map(translateTag);
}

// Mapa invertido (PT -> EN) para busca
const REVERSE_TRANSLATIONS: Record<string, string> = Object.fromEntries(
  Object.entries(TAG_TRANSLATIONS).map(([en, pt]) => [pt.toLowerCase(), en])
);

/**
 * Encontra tags em inglês que correspondem a um termo de busca
 * Busca tanto no termo em português quanto em inglês
 */
export function findMatchingTags(searchTerm: string): string[] {
  const lower = searchTerm.toLowerCase().trim();
  const matches: string[] = [];

  // Busca direta em inglês
  if (TAG_TRANSLATIONS[lower]) {
    matches.push(lower);
  }

  // Busca reversa em português
  if (REVERSE_TRANSLATIONS[lower]) {
    matches.push(REVERSE_TRANSLATIONS[lower]);
  }

  // Busca parcial (contém o termo)
  for (const [en, pt] of Object.entries(TAG_TRANSLATIONS)) {
    if (en.includes(lower) || pt.toLowerCase().includes(lower)) {
      if (!matches.includes(en)) {
        matches.push(en);
      }
    }
  }

  return matches;
}
